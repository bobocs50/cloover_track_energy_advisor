function eur(n: number, opts?: { sign?: boolean; compact?: boolean }) {
  const abs = Math.abs(n);
  const formatted = abs.toLocaleString("de-DE", {
    maximumFractionDigits: 0,
    notation: opts?.compact ? "compact" : "standard",
  });
  if (opts?.sign) return `${n >= 0 ? "+" : "-"}€${formatted}`;
  return `${n < 0 ? "-" : ""}€${formatted}`;
}

function clean(value: string) {
  return value
    .replace(/[*_`]/g, "") // strip leftover markdown emphasis
    .replace(/[\u{FE0F}\u{200D}]/gu, "")
    .replace(/\p{Extended_Pictographic}/gu, "") // strip emoji
    .replace(/[–—]/g, "-")
    .replace(/\s+/g, " ")
    .trim();
}

/* Strip the noise the LLM prose can carry (emoji, stray backticks/underscores)
   without touching the bold/italic emphasis markers we actually want to render. */
function tidy(value: string) {
  return value
    .replace(/[`_]/g, "")
    .replace(/[\u{FE0F}\u{200D}]/gu, "")
    .replace(/\p{Extended_Pictographic}/gu, "")
    .trim();
}

/* Render one line of inline markdown: **bold** and *italic*. No dependency
   added — the advisor prose only ever uses these two markers. */
function inline(text: string, keyBase: string): ReactNode[] {
  const out: ReactNode[] = [];
  const re = /\*\*(.+?)\*\*|\*(.+?)\*/g;
  let last = 0;
  let match: RegExpExecArray | null;
  let i = 0;
  while ((match = re.exec(text)) !== null) {
    if (match.index > last) out.push(text.slice(last, match.index));
    if (match[1] !== undefined) {
      out.push(
        <strong key={`${keyBase}-${i}`} className="font-semibold text-[#0f172a]">
          {match[1]}
        </strong>,
      );
    } else {
      out.push(<em key={`${keyBase}-${i}`}>{match[2]}</em>);
    }
    last = match.index + match[0].length;
    i += 1;
  }
  if (last < text.length) out.push(text.slice(last));
  return out;
}

/* Minimal markdown → paragraphs for the advisor letter: blank lines split
   paragraphs, single newlines collapse, bold and italic render inline. */
function Markdown({ children, className }: { children: string; className?: string }) {
  const paragraphs = tidy(children)
    .split(/\n{2,}/)
    .map((p) => p.replace(/\s*\n\s*/g, " ").trim())
    .filter(Boolean);
  return (
    <div className={className}>
      {paragraphs.map((p, idx) => (
        <p key={idx}>{inline(p, `p${idx}`)}</p>
      ))}
    </div>
  );
}

