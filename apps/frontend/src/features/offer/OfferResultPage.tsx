/* Collapsed by default to the first PREVIEW_COUNT rows — the full list is long,
   technical provenance the customer rarely needs. A "show all / fold up" toggle
   reveals the rest in place. */
const SOURCE_PREVIEW_COUNT = 2;

function SourceList({ assumptions }: { assumptions: Assumption[] }) {
  const [expanded, setExpanded] = useState(false);
  const collapsible = assumptions.length > SOURCE_PREVIEW_COUNT;
  const visible = expanded || !collapsible ? assumptions : assumptions.slice(0, SOURCE_PREVIEW_COUNT);
  const hiddenCount = assumptions.length - SOURCE_PREVIEW_COUNT;

  return (
    <div className="mt-3 rounded-xl border border-[#e7eaf0] bg-white p-4 sm:p-5">
      <p className="mb-1 text-[11px] font-semibold uppercase tracking-[0.1em] text-[#94a3b8]">
        Data &amp; assumptions
      </p>
      <dl className="divide-y divide-[#eef1f6]">
        {visible.map((a) => (
          <div
            key={a.field}
            className="flex flex-wrap items-baseline justify-between gap-x-3 gap-y-0.5 py-2.5"
          >
            <dt className="text-[13px] font-medium text-[#334155]">{assumptionLabel(a.field)}</dt>
            <dd className="text-[13px] font-semibold tabular-nums text-[#0f172a]">
              {clean(a.value)}
            </dd>
            <p className="w-full text-[11px] leading-4 text-[#94a3b8]">{clean(a.source)}</p>
          </div>
        ))}
      </dl>
      {collapsible && (
        <button
          type="button"
          onClick={() => setExpanded((v) => !v)}
          className={`mt-2 flex w-full items-center justify-center gap-1.5 rounded-lg border-t border-[#eef1f6] pt-2.5 text-[12px] font-semibold text-[#64748b] transition-[color,transform] duration-150 ${EASE} hover:text-[#2f6fed] active:scale-[0.99]`}
        >
          {expanded ? (
            <>
              Fold up
              <ChevronDown size={14} strokeWidth={2.6} className="rotate-180" />
            </>
          ) : (
            <>
              <span className="tracking-[0.2em] text-[#94a3b8]">···</span>
              Show all {assumptions.length} assumptions
              <span className="text-[#94a3b8]">(+{hiddenCount})</span>
              <ChevronDown size={14} strokeWidth={2.6} />
            </>
          )}
        </button>
      )}
    </div>
  );
}

