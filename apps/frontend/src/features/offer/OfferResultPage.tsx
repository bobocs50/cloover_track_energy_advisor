function titleCase(key: string) {
  return key
    .replace(/[_-]+/g, " ")
    .trim()
    .replace(/\b\w/g, (c) => c.toUpperCase());
}

function driverLabel(raw: string) {
  return DRIVER_LABELS[raw.trim().toLowerCase()] ?? titleCase(raw);
}

